"""
main.py - A runnable multi-agent "crew" built with CrewAI.

THE STORY THIS CODE TELLS
-------------------------
A single language-model call is a smart generalist with no process. For any
non-trivial task it tends to do everything in one breath: research, write, and
judge its own work all at once. The result is usually shallow and self-
flattering, because the model rarely catches its own mistakes.

A *multi-agent system* splits that one job into specialists that hand work to
each other, exactly like a small newsroom:

    Orchestrator  ->  Researcher  ->  Writer  ->  Critic
                                         ^            |
                                         |____________|
                                      "send it back"

  * Orchestrator - decides the order of work and passes outputs along.
                   In CrewAI this is the Crew + Process itself, not a separate
                   agent you write by hand.
  * Researcher   - gathers and organizes the raw facts.
  * Writer       - turns those facts into a clean article.
  * Critic       - checks the draft and can demand revisions.

This file also includes a FOURTH agent, a Curriculum Designer, whose job is to
turn the crew's work into the teaching content that fills index.html. It outputs
structured JSON (not prose), which we parse and write to content.json. The HTML
page fetches that file, so the interactive lesson is generated BY the crew rather
than hand-written. See the build_curriculum_task() docstring for details.

CrewAI hides the plumbing. You describe WHO the agents are (role / goal /
backstory) and WHAT the tasks are, then wire the tasks together with
`context=[...]`. The framework handles the prompting, the ordering, and the
passing of each result into the next step. The handoff_demo.py file in this
repo does the same thing by hand so you can see what CrewAI abstracts away.

HOW TO RUN
----------
    pip install -r requirements.txt
    cp .env.example .env   # then fill in GEMINI_API_KEY (read in automatically)
    python main.py "your research topic"
"""

import json
import os
import re
import sys

# Loads variables from a .env file in this folder into the environment, so you
# don't have to `export` the API key by hand every session.
from dotenv import load_dotenv

# CrewAI's public building blocks. `LLM` is CrewAI's thin wrapper around
# litellm, which is what lets us point at Google Gemini instead of OpenAI.
from crewai import Agent, Task, Crew, Process, LLM

load_dotenv()


# Where the teaching content the crew produces gets written. index.html fetches
# this file on load.
CONTENT_PATH = os.path.join(os.path.dirname(os.path.abspath(__file__)), "content.json")


def build_llm() -> LLM:
    """Create the shared language model that every agent will use.

    We use Google's Gemini *free tier*. CrewAI talks to it through litellm, and
    the model id `gemini/gemini-2.5-flash` is the signal that routes the call to
    Google rather than to OpenAI. The API key is read from the environment so no
    secret ever lives in the source code.
    """
    api_key = os.environ.get("GEMINI_API_KEY")
    if not api_key:
        # Fail loudly and helpfully. A confusing auth error three layers deep in
        # litellm is the most common thing that trips up first-time learners.
        sys.exit(
            "\nERROR: GEMINI_API_KEY is not set.\n"
            "\nThis project runs for FREE on Google's Gemini free tier.\n"
            "  1. Get a key (free, no credit card): https://aistudio.google.com/apikey\n"
            "  2. Copy .env.example to .env and paste the key in:\n"
            "       cp .env.example .env\n"
            "  3. Re-run:    python main.py \"your topic\"\n"
        )
    # temperature is kept modest so the researcher stays factual; the writer and
    # critic inherit the same model. You could give each agent its own LLM with
    # different settings, which is a common real-world tuning step.
    #
    # Provider note: CrewAI 1.x reaches Gemini through its native google-genai
    # client (listed in requirements.txt). The "gemini/..." model id selects
    # that provider; the client reads GEMINI_API_KEY and uses the same free
    # tier. If you would rather use the older litellm path, run
    # `pip install litellm` and add is_litellm=True to this call.
    #
    # The model defaults to gemini-2.5-flash, which has working free-tier quota
    # as of this writing (gemini-2.0-flash currently shows "limit: 0" on new
    # free-tier keys). Override with GEMINI_MODEL if Google's free-tier lineup
    # changes again, e.g. GEMINI_MODEL=gemini-2.5-flash-lite.
    model_name = os.environ.get("GEMINI_MODEL", "gemini-2.5-flash")
    return LLM(
        model=f"gemini/{model_name}",
        api_key=api_key,
        temperature=0.4,
    )


def build_agents(llm: LLM):
    """Define the four specialists.

    An Agent in CrewAI is essentially a *persona plus a job description*. The
    role/goal/backstory fields are not decoration - they are injected into the
    system prompt and genuinely change how the model behaves. A good backstory
    is the cheapest quality lever you have.
    """

    # --- The Researcher -----------------------------------------------------
    # First specialist in the chain. Its only job is to produce organized,
    # trustworthy raw material. It does NOT write prose for the reader.
    researcher = Agent(
        role="Senior Research Analyst",
        goal=(
            "Find and organize the most important, accurate facts about the "
            "given topic so a writer can turn them into an article."
        ),
        backstory=(
            "You are a meticulous analyst who has spent years separating signal "
            "from hype. You produce tight, well-structured briefs: key points, "
            "context, and any caveats. You never pad and you flag uncertainty."
        ),
        llm=llm,
        # allow_delegation=False keeps each agent in its lane. Turning it on lets
        # an agent ask teammates for help, which is powerful but harder to reason
        # about for a first example.
        allow_delegation=False,
        verbose=True,
    )

    # --- The Writer ---------------------------------------------------------
    # Second in the chain. It consumes the researcher's brief (via task context,
    # wired below) and shapes it into something a human enjoys reading.
    writer = Agent(
        role="Technical Writer",
        goal=(
            "Turn the research brief into a clear, engaging, well-structured "
            "article for a smart but non-expert reader."
        ),
        backstory=(
            "You are a writer who makes complex topics feel simple without "
            "dumbing them down. You favor plain language, concrete examples, and "
            "a logical flow from idea to idea. You write only from the facts you "
            "are given - you do not invent details."
        ),
        llm=llm,
        allow_delegation=False,
        verbose=True,
    )

    # --- The Critic ---------------------------------------------------------
    # Third in the chain. This is the agent that makes multi-agent systems worth
    # the extra cost: a *separate* set of eyes that was not invested in writing
    # the draft, so it is far more willing to find fault.
    critic = Agent(
        role="Editorial Critic",
        goal=(
            "Rigorously review the draft for accuracy, clarity, and structure, "
            "then return a polished final version - or send it back with specific "
            "fixes if it is not good enough."
        ),
        backstory=(
            "You are a demanding editor. You check claims against the research "
            "brief, hunt for vague sentences, and refuse to let weak writing "
            "ship. When the draft is close, you fix it yourself; when it is not, "
            "you say exactly what must change and why."
        ),
        llm=llm,
        allow_delegation=False,
        verbose=True,
    )

    # --- The Curriculum Designer -------------------------------------------
    # Fourth agent. Unlike the others it does NOT write prose - it emits a
    # structured data file that the HTML teaching page consumes. This is a common
    # real-world role: an agent whose output is machine-readable so a downstream
    # system (here, a web page) can render it. Note the emphasis in the backstory
    # on returning ONLY valid JSON; structured-output agents live or die by how
    # strictly they obey the format contract.
    curriculum_designer = Agent(
        role="Curriculum Designer",
        goal=(
            "Convert the team's understanding of multi-agent systems into a "
            "structured JSON lesson that a web page can render: agent roles, a "
            "glossary, and a step-by-step simulated run."
        ),
        backstory=(
            "You are an instructional designer who turns expert material into "
            "clear lessons for non-technical people. You think in structured "
            "data, not paragraphs. When asked for JSON you return ONLY valid "
            "JSON - no commentary, no markdown code fences, nothing else."
        ),
        llm=llm,
        allow_delegation=False,
        verbose=True,
    )

    return researcher, writer, critic, curriculum_designer


def build_tasks(topic, researcher, writer, critic, curriculum_designer):
    """Define the work and, crucially, how the work HANDS OFF.

    The single most important idea here is `context=[...]`. It is the wiring that
    turns separate agents into a pipeline: the output of one task is fed in as
    background for the next. This is the "handoff" - the same baton-passing that
    handoff_demo.py does manually with f-strings.
    """

    # STEP 1: research. No context, because this is the start of the chain.
    research_task = Task(
        description=(
            f"Research the topic: '{topic}'.\n"
            "Produce a structured brief with 5-8 key points. For each point give "
            "one or two sentences of explanation. Note any important caveats or "
            "areas of disagreement. Keep it factual and organized, not prose."
        ),
        expected_output=(
            "A bulleted research brief of 5-8 key points with short explanations "
            "and any caveats."
        ),
        agent=researcher,
    )

    # STEP 2: write. THE HANDOFF: context=[research_task] means the writer
    # receives the researcher's brief automatically. The writer never has to ask
    # for it - the orchestrator delivers it.
    writing_task = Task(
        description=(
            "Using ONLY the research brief provided to you, write a clear, "
            "engaging article of about 400-500 words for a smart non-expert. "
            "Give it a title, a short intro, a few body sections, and a brief "
            "conclusion. Do not invent facts that are not in the brief."
        ),
        expected_output="A well-structured ~400-500 word article in Markdown.",
        agent=writer,
        context=[research_task],  # <-- handoff: researcher -> writer
    )

    # STEP 3: critique + finalize. THE SECOND HANDOFF plus the FEEDBACK LOOP.
    # The critic sees BOTH the original brief and the draft (context has two
    # items), so it can fact-check the draft against the source material - the
    # core defense against a "hallucination cascade" where one agent's mistake
    # gets amplified downstream.
    #
    # ABOUT THE "SEND IT BACK" LOOP:
    # In this sequential crew the loop is logical, not a literal re-run: we
    # instruct the critic to internally decide "is this good enough?" and, if
    # not, to revise it itself before returning the final version. To make the
    # loop *automatic* (writer actually re-runs on rejection) you would switch
    # the crew to `Process.hierarchical` with a manager agent, or build an
    # explicit loop in code as handoff_demo.py shows. We keep it sequential here
    # because it is the clearest possible first example.
    critique_task = Task(
        description=(
            "Review the draft article against the original research brief.\n"
            "1. Check every claim in the draft against the brief. Flag anything "
            "unsupported.\n"
            "2. Judge clarity, structure, and flow.\n"
            "3. Decide: is this good enough to publish?\n"
            "   - If NO: explain the specific problems, then rewrite the article "
            "to fix them yourself (this is the 'send it back and improve' loop).\n"
            "   - If YES: lightly polish it.\n"
            "Return only the final, publication-ready article."
        ),
        expected_output="The final, polished, publication-ready article in Markdown.",
        agent=critic,
        context=[research_task, writing_task],  # <-- sees brief AND draft
    )

    # STEP 4: design the teaching content as JSON. THE THIRD HANDOFF: this task
    # gets the research brief AND the final article as context, so the lesson it
    # designs is grounded in the crew's own work. Its output is the data that
    # index.html renders.
    curriculum_task = build_curriculum_task(
        curriculum_designer, topic, context=[research_task, critique_task]
    )

    return research_task, writing_task, critique_task, curriculum_task


def build_curriculum_task(curriculum_designer, topic, context):
    """Define the structured-output task.

    The prompt is unusually strict on FORMAT because the consumer is a program,
    not a person. We spell out the exact JSON shape and the exact glossary
    terms. The simulation is scripted around the REAL topic passed on the
    command line (not a fixed example), so the page's walkthrough matches
    whatever you actually researched. The schema here MUST match what
    index.html reads in its render functions, and the FALLBACK_CONTENT dict
    below, which is the safety net if the model ever returns something
    unparseable.
    """
    return Task(
        description=(
            "Design the content for an interactive web page that teaches non-"
            "technical coworkers how a multi-agent system works (an orchestrator "
            "delegating to researcher, writer, and critic agents, where the "
            "critic can send work back).\n\n"
            "Return ONLY a single valid JSON object - no markdown, no code "
            "fences, no text before or after. The object MUST have exactly these "
            "three top-level keys:\n\n"
            "1. \"agents\": an array of EXACTLY 4 objects, in this order, with "
            "these keys:\n"
            "   - \"key\": one of \"orchestrator\", \"researcher\", \"writer\", "
            "\"critic\" (in that order)\n"
            "   - \"tag\": a short label (\"Manager\", \"Step 1\", \"Step 2\", "
            "\"Step 3\")\n"
            "   - \"name\": the display name (\"Orchestrator\", \"Researcher\", "
            "\"Writer\", \"Critic\")\n"
            "   - \"role\": one short sentence naming the role\n"
            "   - \"goal\": one short sentence on what it tries to achieve\n"
            "   - \"plain\": 2-3 sentences of plain-language explanation for a "
            "non-technical reader\n\n"
            "2. \"glossary\": an array of objects, one for EACH of these terms in "
            "this order: Orchestrator, Agent, Tool, MCP (Model Context "
            "Protocol), A2A (Agent-to-Agent), Handoff. Each object has \"term\" "
            "and \"definition\" (one or two plain-language sentences).\n\n"
            f"3. \"simulation\": an array that scripts a step-by-step run of the "
            f"crew on the request 'Research {topic} and write a short summary for "
            f"our team.' Base it on the actual research and article the crew just "
            f"produced (see context) - use real findings about {topic}, not a "
            f"generic placeholder. Each element has:\n"
            "   - \"role\": one of \"orchestrator\", \"researcher\", \"writer\", "
            "\"critic\", \"final\"\n"
            "   - \"label\": display name for that turn\n"
            "   - \"step\": a short progress label (e.g. \"Step 1 of 3\", "
            "\"Looping back\", \"Delivered\")\n"
            "   - \"body\": what that agent says/produces on this turn (plain "
            "text; you may use \\n for line breaks)\n"
            "   - optionally \"reject\": true on the critic turn where it sends "
            "the draft back\n"
            "   - optionally \"approve\": true on the critic turn where it "
            "approves\n"
            "   - optionally \"final\": true on the last element (the delivered "
            "answer)\n"
            "   The simulation MUST include, in order: an orchestrator planning "
            "step, a researcher step, a writer step, a critic step that REJECTS "
            "with reject=true and explains why, an orchestrator 'looping back' "
            "step, a second researcher step, a second writer step, a critic step "
            "that APPROVES with approve=true, and a final element with "
            "final=true containing the polished answer.\n\n"
            f"Keep all writing in sentence case, plain language, no emoji. Make "
            f"the {topic} content concrete and genuinely useful, grounded in "
            f"what the researcher and writer actually produced."
        ),
        expected_output=(
            "A single valid JSON object with keys agents, glossary, and "
            "simulation, and nothing else."
        ),
        agent=curriculum_designer,
        context=context,  # <-- grounded in the research brief + final article
    )


