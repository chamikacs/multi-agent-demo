def save_curriculum(curriculum_output, topic):
    """Parse the curriculum agent's raw output and persist content.json.

    This is the try/except + sane fallback the lesson is built around: we attempt
    to extract and validate the model's JSON; if anything goes wrong we log it and
    write the known-good FALLBACK_CONTENT so the teaching page never breaks.
    """
    # The "incoming request" banner index.html shows above the simulation is
    # built from the real topic here, in Python, rather than asked of the
    # model - it is plain string formatting, so it is always correct and
    # always matches what was actually run, even if the LLM's JSON is wonky.
    request_line = f"Research {topic} and write a short summary for our team."
    raw = getattr(curriculum_output, "raw", None) or str(curriculum_output)
    try:
        data = validate_content(extract_json(raw))
        data["request"] = request_line
        write_content(data, source="crew")
    except (ValueError, json.JSONDecodeError) as err:
        print(f"\n[content] could not parse crew JSON ({err}); using fallback content.")
        fallback = dict(FALLBACK_CONTENT)
        fallback["request"] = request_line
        write_content(fallback, source="fallback")


def main() -> None:
    # The topic comes from the command line; everything after the script name is
    # joined so quoting is optional. Falls back to a sensible default.
    topic = " ".join(sys.argv[1:]).strip() or "multi-agent AI systems"

    print(f"\n=== Multi-agent crew starting on topic: {topic!r} ===\n")

    llm = build_llm()
    researcher, writer, critic, curriculum_designer = build_agents(llm)
    research_task, writing_task, critique_task, curriculum_task = build_tasks(
        topic, researcher, writer, critic, curriculum_designer
    )

    # The Crew is the ORCHESTRATOR. `Process.sequential` means: run the tasks in
    # order, and (because we wired context=[...]) feed each result forward. The
    # crew is what turns a pile of agents and tasks into a working assembly line.
    crew = Crew(
        agents=[researcher, writer, critic, curriculum_designer],
        tasks=[research_task, writing_task, critique_task, curriculum_task],
        process=Process.sequential,
        verbose=True,
    )

    # kickoff() runs the whole pipeline. The CrewOutput it returns exposes every
    # task's result in .tasks_output, so we can grab BOTH the critic's final
    # article and the curriculum designer's JSON.
    result = crew.kickoff()

    # tasks_output is in the same order as the tasks list above.
    article_output = result.tasks_output[2]      # critic's final article
    curriculum_output = result.tasks_output[3]   # curriculum designer's JSON

    # Persist the teaching content (with parsing fallback) for index.html.
    save_curriculum(curriculum_output, topic)

    print("\n\n========== FINAL ARTICLE ==========\n")
    print(article_output)
    print("\n===================================\n")
    print("Open index.html to see the crew-generated lesson (it reads content.json).")


