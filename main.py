# Parsing the curriculum agent's output into content.json
# ---------------------------------------------------------------------------

def extract_json(text):
    """Pull a JSON object out of the model's raw text.

    Even when told 'JSON only', models sometimes wrap output in ```json fences or
    add a stray sentence. This is defensive plumbing: strip common code fences,
    then fall back to grabbing the outermost {...} span. Raises ValueError if no
    valid JSON object can be recovered, which the caller turns into a fallback.
    """
    if not text:
        raise ValueError("empty model output")

    cleaned = text.strip()

    # Remove a leading/trailing markdown code fence if present.
    fence = re.match(r"^```(?:json)?\s*(.*?)\s*```$", cleaned, re.DOTALL)
    if fence:
        cleaned = fence.group(1).strip()

    # First try: parse the whole thing.
    try:
        return json.loads(cleaned)
    except json.JSONDecodeError:
        pass

    # Second try: grab the outermost brace span and parse that.
    start = cleaned.find("{")
    end = cleaned.rfind("}")
    if start != -1 and end != -1 and end > start:
        return json.loads(cleaned[start : end + 1])

    raise ValueError("no JSON object found in model output")


def validate_content(data):
    """Light structural check so we never write garbage to content.json.

    We only assert the shape index.html depends on. If anything essential is
    missing we raise, and the caller writes the known-good FALLBACK_CONTENT
    instead. Better a correct hand-written lesson than a half-broken page.
    """
    if not isinstance(data, dict):
        raise ValueError("top level is not an object")
    for key in ("agents", "glossary", "simulation"):
        if key not in data or not isinstance(data[key], list) or not data[key]:
            raise ValueError(f"missing or empty '{key}'")
    if len(data["agents"]) != 4:
        raise ValueError("expected exactly 4 agents")
    for a in data["agents"]:
        for field in ("key", "tag", "name", "role", "goal", "plain"):
            if not a.get(field):
                raise ValueError(f"agent missing '{field}'")
    for g in data["glossary"]:
        if not g.get("term") or not g.get("definition"):
            raise ValueError("glossary entry missing term/definition")
    for s in data["simulation"]:
        for field in ("role", "label", "step", "body"):
            if not s.get(field):
                raise ValueError(f"simulation step missing '{field}'")
    return data


def write_content(data, source):
    """Write the validated content to content.json and report where it came from."""
    payload = dict(data)
    payload["_meta"] = {"source": source}  # so the page/dev can see the origin
    with open(CONTENT_PATH, "w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2, ensure_ascii=False)
    print(f"\n[content] wrote {CONTENT_PATH} (source: {source})")


